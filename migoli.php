                  <h1 class="page-header">Progetto Amici di Migoli</h1>
                     <p>
                     <b>Changarawe - Migoli</b> è una circoscrizione mista della Tanzania centrale situata nel distretto rurale di Iringa, in una zona arida e con scarse risorse economiche. La popolazione è a maggioranza Masai e conta circa 18.000 persone. 
                     <br>
                     Le <b>Suore Collegine della Sacra Famiglia</b> gestiscono un piccolo Health Center, gli alloggi per i volontari e l'asilo. 
                     <br>
                     <br>
                     <b>Dall'agosto del 2019</b>, grazie al contributo di molti amici e il sostegno delle Associazioni KAMMEA e PAMOYA, sono stati realizzati innanzitutto lavori di <b>manutenzione urgente</b> come rifacimento dei tetti, strutture di sostegno e controsoffittature, ammodernamento degli impianti idrico ed elettrico che hanno consentito la ripartenza delle attività nell'Health Center garantendo alla popolazione l'assistenza sanitaria primaria. 
                     <br>
                     <br>
                     Successivamente è stato possibile eseguire anche <b>lavori straordinari</b> che hanno migliorato le prestazioni offerte e l'efficienza del centro. 
                     <br>
                     In particolare, si è provveduto a:
                     <ul>
                        <li>messa in opera dei <b>pannelli solari</b> che rendono sia l'Health Center che la casa della Missione indipendente dalla linea elettrica nazionale, soggetta a continui blackout</li>
                        <li>ammodernamento del <b>reparto di maternità</b></li>
                        <li>ristrutturazione della <b>sala parto</b> e della sterilizzazione </li>
                        <li>creazione di un reparto di <b>Emergency Care</b> </li>
                        <li>riorganizzazione del <b>laboratorio analisi</b> </li>
                        <li>ristrutturazione della <b>casa di accoglienza</b> per i volontari </li>
                     </ul>
                     I <b>progetti attualmente</b> in corso prevedono il <b>rifacimento dell'asilo</b> e l'<b>allestimento</b> di una piccola <b>sala operatoria</b> per poter eseguire tagli cesarei e cure chirurgiche minori.
                     <br>
                     Il <b>Progetto “Amici di Migoli”</b> nasce dalla passione di un medico italiano, il dottor Giovanni Cecchini che, insieme alla moglie Daniela, ha dedicato gran parte della sua vita, professionale e non, alla popolazione africana. 
                     <br>
                     L'entusiasmo di Giovanni e Daniela ha coinvolto i consuoceri Angelo e Lella Aimetti che hanno raccolto il testimone e hanno coinvolto sempre più amici per poter sostenere la comunità di Changarawe, offrendo risorse umane e materiali per la crescita dell'Health Center e dell'asilo, nell'ottica della <b>promozione del diritto alla salute e all'istruzione</b>, diritti primari per la persona e fondamentali per lo sviluppo dell'intera nazione. 
                     <br>
                     Tutte le opere sono realizzate in collaborazione con le Suore Collegine che vivono a Changarawe ed eseguite con manodopera locale. 
                     <br>
                     <br>
                     Dal 2023 il Progetto Amici di Migoli è stato ufficialmente incluso nella mission della Associazione PAMOYA – INSIEME PER CRESCERE ONLUS, di Malonno (BS), che fin da subito ha sostenuto il progetto mettendo a disposizione competenze e mezzi logistici.
