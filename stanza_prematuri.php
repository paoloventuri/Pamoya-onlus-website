                     <h3>Progetto</h3>
                     Il progetto di ampliare la stanza per i neonati con un peso basso alla nascita e adibirne una per i neonati richiedenti assistenza specifica, prevede lo spostamento della stanza per la KMC, adesso adibita ad entrambi i servizi, in una stanza più grande provvista di servizi igienici adesso destinata al ricovero delle mamme dopo parto.
                     Si farà in modo che nella stanza sia mantenuta una temperatura adeguata per il ricovero delle madri con i loro bambini e per questo motivo saranno cambiate le finestre e la porta e isolate le pareti.
                     In tale stanza le mamme saranno istruite circa la tecnica della Kangaroo Mother Care.
                     Inoltre sarà presente e a disposizione delle mamme e del personale sanitario i protocolli e le linee guida della tecnica stessa.
                     Il personale sanitario sarà formato adeguatamente sulla tecnica della KMC e la cura del neonato con basso peso alla nascita.
                     Saranno compilati fogli apposta per il controllo dei parametri vitali, l’alimentazione, il peso, gli input e output del neonato.
                     Saranno presenti le cartelle cliniche di ciascun bimbo. 
                     La stanza sarà dotata di propri ausili per la pulizia e sarà inoltre possibile cambiarsi le scarpe all’entrata in modo tale da tenere la stanza il più pulita possibile.
                     La piccola stanza che adesso offre entrambi i servizi sarà invece destinata all’osservazione e al trattamento dei neonati che richiedono una particolare assistenza e che non possono usufruire della KMC (neonati con un peso inferiore a 1200 g, neonati con sepsi o patologie di varia origine).
