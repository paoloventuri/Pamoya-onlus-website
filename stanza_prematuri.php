                     All’interno del reparto di maternità del Saint Joseph Hospital si trova una stanza adibita all’assistenza del neonato pretermine (nato prima della 37a settimana di gestazione) e/o con peso alla nascita inferiore a 2.5 kg, causa del 60-80 % delle morti perinatali secondo i dati della World Health Organization e di un’alta probabilità di morbilità perinatale.
                     Questa categoria di neonati, infatti, richiede un’assistenza specifica per quanto riguarda la nutrizione, il mantenimento della temperatura corporea, l’igiene generale e del cordone ombelicale e la diagnosi e il trattamento precoce di eventuali infezioni e complicazioni.
                     Inoltre nella stanza vengono ospitati neonati che hanno bisogno di assistenza specifica e continua come nel caso neonati portatori di patologie e malattie infettive, neonati con un ritardo di crescita o un distress fisico dovuto a varie cause come l’asfissia neonatale, neonati con un indice di Apgar inferiore a 7 dopo la nascita e ai neonati orfani in attesa di un care giver.
                     <br>
                     <img class="center" src="img/prematuri/prematuri_01.jpg" oncontextmenu="return false;">
                     <br>
                     Una delle tecniche utilizzate per l’assistenza al neonato con un peso alla nascita inferiore a 2.5 kg è la Kangaroo Mother Care (KMC) (vedi Appendice) che prevede il contatto pelle a pelle tra madre e bambino 24 ore su 24 durante il ricovero ospedaliero e/o la permanenza a casa.
                     Questa tecnica, oltre ad essere ritenta una delle migliori in termini di sicurezza ed efficacia anche qualora l’ospedale offrisse strumenti specifici come l’incubatrice, risulta essere la scelta più economica laddove vi sono insufficienti risorse.
                     Al momento la stanza offre un piccolo spazio nel quale sono disposti due letti destinati al ricovero della madre accompagnatrice del neonato, un unico lettino con spondine provvisto di un materasso per i neonati che hanno bisogno di assistenza specifica, un mobile con alcuni strumenti per l’assistenza al neonato, una bilancia e una stufa elettrica che riscalda la stanza.
                     I problemi relativi a suddetta stanza sono:
                     <ul>
                        <li>Lo spazio: la stanza ha la capienza di al massimo due letti, insufficienti a ospitare altre mamme per la KMC nel caso ci fossero più di due bambini con un peso alla nascita inferiore a 2500 g. Inoltre il poco spazio costringe a mettere vicini neonati prematuri e neonati portatori di patologie talvolta contagiose.</li>
                        <li>La dispersione termica: la finestra e la porta sono di legno, e questo è insufficiente al mantenimento di una temperatura adeguata senza un eccessivo consumo della corrente elettrica che alimenta la stufa.</li>
                        <li>La stufa che viene utilizzata non è sicura e comporta un eccessivo utilizzo di energia elettrica (durante le ore notturne consuma più dell’intero ospedale).</li>
                        <li>Inadeguato materiale per un’assistenza specifica al neonato.</li>
                        <li>Mancanza di lavandino e acqua per il lavaggio sociale delle mani.</li>
                        <li>Mancanza di servizi igienici destinati solo alle mamme che stanno a contatto con i neonati prematuri.</li>
                     </ul>
                     Il progetto di ampliare e migliorare la stanza per i prematuri nasce quindi non sono dall’esigenza di migliorare la qualità del servizio ma anche da un aumentato numero di nascite e trasferimenti di neonati con un peso alla nascita inferiore ai 2.5 kg. 
                     Il Saint Joseph Hospital di Ikelu infatti è l’unico ospedale della zona ad offrire una stanza riscaldata, seppur piccola, per i neonati pretermine e/o con basso peso alla nascita. 
                     Da Agosto 2017 ad Agosto 2018 sono stati registrati 90 bambini con un peso alla nascita inferiore ai 2.5 kg e 14 morti perinatali su un totale di 1229 nascite.
                     <br>
                     Secondo una media calcolata con il numero totale di neonati nati con un peso inferiore a 2.5 kg diviso i mesi di un anno, la stanza accoglierebbe circa 7 madri con 7 neonati a carico in un mese per la durata della Kangaroo Mother Care per un periodo di degenza che può durare settimane e questo dimostra un inadeguato spazio.
                     Solo il 12% dei 90 neonati con un peso alla nascita inferiore a 2.5 kg da Agosto 2017 ad Agosto 2018 ha usufruito della KMC.
