                     <h3>La Kangaroo Mother Care (KMC)</h3>
                     La tecnica della Kangaroo Mother Care (KMC), destinata ai bambini con un peso alla nascita inferiore ai 2.5 kg e con parametri vitali stabili, prevede contatto pelle a pelle tra madre e bambino 24 ore su 24.
                     Questo permette una stabilizzazione dei parametri cardio respiratori del bambino e una regolazione del livello della sua temperatura corporea meglio di qualsiasi strumento.  
                     La tecnica include anche l’allattamento al seno esclusivo e frequente. 
                     Le evidenze scientifiche sull’utilizzo della Kangaroo Mather Care sui neonati con peso basso ala nascita senza nessun problema medico hanno dimostrato che:
                     <lu>
                        <li>La KMC si può paragonare alle cure convenzionali (incubatrici), in termini di sicurezza e protezione termica utilizzando come parametro la mortalità.</li>
                        <li>La KMC, facilitando l’allattamento materno, offre evidenti vantaggi in caso di severa morbilità. La tecnica contribuisce a l’umanizzazione della cura neonatale e ad un miglior legame tra madre e neonato.</li>
                        <li>La KMC, sembra abbia un effetto benefico anche sul benessere della madre in termini di stress e autostima. Aumenta l’empowerment in quanto la madre si sente partecipe e collaborante nell’aiutare il proprio bambino facendo qualcosa di positivo per lui.</li>
                     </lu>   
                     Secondo alcuni studi, quindi, si conclude che la KMC deve essere incoraggiata, sopratutto nei paesi in cui mancano altri strumenti per la gestione del basso peso alla nascita in quanto migliora l’outcome non solo neonatale ma anche materno e familiare.
